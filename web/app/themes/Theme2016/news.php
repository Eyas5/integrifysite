            <p>Hello Hobby is a digital peer-to-peer marketplace for hobbies - kind of like AirbBnB for hobbies. The company was founded in October 2015 on the idea that hobbies and spare time activities should be fun to do, easy to find, and best when shared with friends or family. We want to get people out of their homes, to do more hobbies, to be more social, and in general to be more active in their lives. The service is currently under testing in a beta and will be launching for public some time in the spring 2016.</p>
