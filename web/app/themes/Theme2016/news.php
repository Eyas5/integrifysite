            <p>“Build, measure, learn!“, says Tuomas Pättö, CEO and founder of <a href="https://hellohobby.co" target="_blank">Hello Hobby</a>, presenting the lean methodology to eager participants at Integrify’s one day workshop for asylum seekers.</p>
            
            <h3 class="newsheading">So what’s going on?</h3>
            
            <p>Well, it’s a normal wednesday afternoon yet there is a lot of buzz at Integrify - it’s time to kick-off the first partner day. During the day, five participants of the Integrify developer program get an opportunity to dive into the depths of business design and lean startups with the help of Tuomas Pättö. “When Integrify reached out to me and  asked us to join I really felt that I want to help the asylum seekers any way I can, and this is the best way I know how to do it. “, Tuomas explains.</p>
            <p>“Lean” business design is a way of developing products, services and businesses by understanding customers, their needs and their lives. Make an educated guess and then iterate by building something, testing it with customer and learning from them. The philosophy, made famous by Eric Ries, the author of The Lean Startup, is something that aspiring entrepreneurs all around the world are almost religiously studying with the hopes of becoming the next Zuckerberg.</p>
        </div>

        <h3 class="newsheading3">Ready, steady, go!</h3>
        <div class="newstext">
            <p>It does not take long for the participants to get excited about the topic at hand. Lean is a way of doing - it’s a state of mind.</p>
            <p>Eyas Taha, 22, from Baghdad, who has previously worked for a start-up, explains that the things he learned in the workshop will help him on his journey to become an entrepreneur one day. “Amazing!”, he says with a smile on his face, “I can use these tools concretely in the future, this is not just about someone talking nice things, but actually learning and doing.”</p>
            <p>Soon, after telling what lean is, Tuomas Pättö jumps to lean methods and tools. “Rapid experimentation &amp; iteration! Believe that you can make a difference! Failure equals progress!” he tells the crowd. You can see concentrated and devoted eyes around him.</p>
            <p>After going through the lean methodology and process it’s time to move on and actually use the tools taught. The participants are divided into two groups with the task to come up with a business idea and to work on it for the rest of the day.</p>
            <p>One team came up with an idea of renting your own bike, kind of like airbnb for bikes, and the other team is planning to develop a mobile application to switch all of the lights on and off in your apartment. “Apply customer learnings in developing your ideas”, says Pättö, and encourages the guys to get out of the building and get the product and the vision tested with real people.</p>
        </div>

